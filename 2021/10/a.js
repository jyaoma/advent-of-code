const errors = {
  round: 0,
  square: 0,
  curly: 0,
  angle: 0,
};

input.forEach((line) => {
  const memory = [];

  let found = false;

  line.split("").forEach((char) => {
    if (!found) {
      if (char === "(" || char === "[" || char === "{" || char === "<") {
        memory.push(char);
      } else if (char === ")") {
        if (memory.pop() !== "(") {
          found = true;
          errors.round++;
          return;
        }
      } else if (char === "]") {
        if (memory.pop() !== "[") {
          found = true;
          errors.square++;
          return;
        }
      } else if (char === "}") {
        if (memory.pop() !== "{") {
          found = true;
          errors.curly++;
          return;
        }
      } else if (char === ">") {
        if (memory.pop() !== "<") {
          found = true;
          errors.angle++;
          return;
        }
      }
    }
  });
});

const answer =
  errors.round * 3 +
  errors.square * 57 +
  errors.curly * 1197 +
  errors.angle * 25137;

console.log(answer);
