const scores = [];

input.forEach((line) => {
  const memory = [];

  let found = false;

  line.split("").forEach((char) => {
    if (char === "(" || char === "[" || char === "{" || char === "<") {
      memory.push(char);
    } else if (char === ")" && memory.pop() !== "(") {
      found = true;
    } else if (char === "]" && memory.pop() !== "[") {
      found = true;
    } else if (char === "}" && memory.pop() !== "{") {
      found = true;
    } else if (char === ">" && memory.pop() !== "<") {
      found = true;
    }
  });

  if (!found) {
    memory.reverse();
    let score = 0;
    memory.forEach((char) => {
      score *= 5;
      if (char === "(") score++;
      else if (char === "[") score += 2;
      else if (char === "{") score += 3;
      else if (char === "<") score += 4;
    });
    scores.push(score);
  }
});

const answer = scores.sort((a, b) => a - b)[scores.length / 2 - 0.5];

console.log(answer);
