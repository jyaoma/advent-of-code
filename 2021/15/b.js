const rawInput = inputPersonal;

const duplicatedInput = [];
for (let y = 0; y < rawInput.length * 5; y++) {
  const originalY = y % rawInput.length;
  const yOffset = Math.floor(y / rawInput.length);
  for (let x = 0; x < rawInput[0].length * 5; x++) {
    const originalX = x % rawInput[0].length;
    const xOffset = Math.floor(x / rawInput.length);

    if (!duplicatedInput[y]) duplicatedInput[y] = [];

    const newValue = rawInput[originalY][originalX] + yOffset + xOffset;
    duplicatedInput[y][x] = newValue > 9 ? newValue - 9 : newValue;
  }
}

const input = duplicatedInput.map((line) =>
  line.map((risk) => ({
    risk,
    min: Infinity,
    minDirection: null,
  }))
);

let newNodes = [[input[0].length - 1, input.length - 1]];

input[input.length - 1][input[0].length - 1].min = 0;

while (newNodes.length) {
  const currentNodes = newNodes.slice();
  newNodes = [];

  currentNodes.forEach(([x, y]) => {
    const newRisk = input[y][x].min + input[y][x].risk;

    if (x > 0) {
      // left
      if (newRisk < input[y][x - 1].min) {
        input[y][x - 1].min = newRisk;
        input[y][x - 1].minDirection = "right";
        newNodes.push([x - 1, y]);
      }
    }
    if (x < input[0].length - 1) {
      // right
      if (newRisk < input[y][x + 1].min) {
        input[y][x + 1].min = newRisk;
        input[y][x + 1].minDirection = "left";
        newNodes.push([x + 1, y]);
      }
    }
    if (y > 0) {
      // up
      if (newRisk < input[y - 1][x].min) {
        input[y - 1][x].min = newRisk;
        input[y - 1][x].minDirection = "down";
        newNodes.push([x, y - 1]);
      }
    }
    if (y < input.length - 1) {
      // down
      if (newRisk < input[y + 1][x].min) {
        input[y + 1][x].min = newRisk;
        input[y + 1][x].minDirection = "up";
        newNodes.push([x, y + 1]);
      }
    }
  });
}

